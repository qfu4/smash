		<h2>Netflix</h2>
		<p>
			Netflix, Inc. is an American media-services provider headquartered in Los Gatos, California, founded in 1997 by Reed Hastings and Marc Randolph in Scotts Valley, California.The Company operates through three segments: Domestic streaming, International streaming and Domestic DVD. As of January 2019, Netflix had over 139 million paid subscriptions worldwide, including 58.49 million in the United States, and over 148 million subscriptions total including free trials. It is available almost worldwide except in mainland China, Syria, North Korea, Iran, and Crimea. The company also has offices in the Netherlands, Brazil, India, Japan, and South Korea.
