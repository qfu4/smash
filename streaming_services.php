		<h2>Hulu</h2>
		<p>
			Hulu was founded in 2007. It is primarily oriented towards instant streaming of television series, carrying current and past episodes of many series from its owners' respective television networks and other content partners. Hulu is currently offered only to users in the United States, its territories and Japan. The CEO of the company Randy Freer announced that by the end of 2018 Hulu had around 25 million subscribers.
		</p>

		<h2>Youtube TV</h2>
		<p>
			YouTube TV began streaming in April, 2017 in five U.S. markets - New York City, Los Angeles, Chicago, Philadelphia and San Francisco. In addition to national U.S. networks, YouTube TV broadcasts channels owned by those networks, their corporate owners and other media companies. YouTube TV is a commercial service that streams more than 60 live TV networks via the Internet. A subsidiary of Alphabet Inc., parent company of Google, YouTube TV's line up includes major U.S. networks such as ABC, CBS, NBC, Fox, CNN, TBS and ESPN. The service expanded to cover 98 percent of U.S. households in January 2019.
